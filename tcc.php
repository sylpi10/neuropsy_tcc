<section id="pour-qui" class="who ttc-section">

    <h2 class="shadow">pour qui ?</h2>
    <div class="text ttc-area text1">
        <ul>
            <div class="text-wrap" data-toggle="tooltip">
                <li>Attaque de panique
                </li>
                <div class="definition">
                    <p>
                        Le trouble panique se manifeste par des attaques de panique fréquentes qui surviennent brusquement en l’absence de toute menace extérieure apparente. Les crises arrivent brutalement et atteignent leur intensité maximale en quelques minutes. Ces attaques provoquent une sensation de mort imminente, de devenir fou, de danger et un besoin irrépressible de s’enfuir.
                    </p>
                    <p>
                        Ce trouble peut grandement perturber la qualité de vie de la personne. Si la personne ne fait rien avec le temps cela devient de plus en plus handicapant.
                    </p>
                </div>
            </div>
            <div class="text-wrap" data-toggle="tooltip">
                <li>Misophonie
                </li>
                <div class="definition">
                    <p>
                        La misophonie est décrite comme étant un état chronique caractérisé par une aversion à certains sons produits par un autre individu, tels que les bruits gutturaux, nasaux ou buccaux (mâchement de gomme, claquement des lèvres), le clic du stylo, ou le tapotement des doigts sur la table ou sur un clavier.1–3 Ce trouble est à distinguer des acouphènes (origine auditive ou neurologique) ou de la phonophobie qui est une crainte d’entendre due à une sensation auditive insupportable, produite par les sons environnants.
                    </p>
                    <p>
                        Les sons impliqués dans la misophonie ont le point commun d’être répétitifs, provoquant une réaction aversive immédiate avec une anxiété, une colère et un sentiment de dégoût important, accompagnés d’une envie impulsive de faire cesser ce bruit de manière immédiate afin de soulager ses symptômes.2 De plus, la majorité des sujets souffrant de misophonie relatent que les bruits émanant d’enfants ou d’animaux ne sont pas incommodants, et que les individus sources des bruits aversifs sont ainsi des adultes dans la totalité des cas.
                    </p>
                </div>
            </div>

            <div class="text-wrap">
                <li> Phobie spécifique ou peurs</li>
                <div class="definition">
                    <p>
                        La phobie est caractérisée par la peur irrationnelle d’une situation particulière, elle est beaucoup plus intense et va au-delà de la simple peur. L’anxiété qui accompagne la phobie peut évoluer jusqu’à l’attaque de panique. Ce qui entraine un évitement de la situation redoutée.
                    </p>
                    <p>
                        Les conséquences sur la vie de la personne sont dépendantes du thème de la phobie.
                    </p>
                    <p>
                        <b> Les phobies de type animales</b> = peur induite par les animaux ou les insectes,
                    </p>
                    <p>
                        <b>Les phobies de type environnement naturel</b> = peur provoquée par des éléments naturels comme les orages, l’eau,
                    </p>
                    <p><b>Les phobies sang/injection/accident</b>= Dans la situation anxiogène, le rythme cardiaque ralentit, la personne peut s’évanouir (ce qui est complètement impossible pour toutes les autres phobies, car le mécanisme est inverse). </p>
                    <p>
                        <b>Les phobies situationnelles</b> interviennent dans des situations particulières comme un voyage en avion ou d’autres moyens de transport, les tunnels ou même les ascenseurs.
                    </p>
                    <p>
                        <b>Les phobies intéroceptives</b> correspondent à la peur des maladies et des sensations corporelles qui sont interprétées comme preuves d’une maladie.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Agoraphobie</li>
                <div class="definition">
                    <p>
                        L’agoraphobie correspond à la peur de certain/e/s situations/lieux dans lesquels/le/s il semble difficile de s’échapper ou d’être secouru/e/ s’il arrivait quelque chose à la personne. Par exemple dans les endroits clos, au milieu d’une foule, dans les transports en commun, seul/e à l’extérieur du domicile.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Phobie sociale, peur du jugement</li>
                <div class="definition">
                    <p>
                        Les situation sociales sont problématiques et déclenchent de l’anxiété. Elles sont classées en 5 catégories :
                    </p>
                    <p>
                        <b>Situation de performance</b> : passer un examen, faire un exposé…
                    </p>
                    <p>
                        <b>Situation d’observation</b> : être regardé ou se croire regarder (salle d’attente, manger en public …)
                    </p>
                    <p>
                        <b>Situation d’affirmation</b> : donner son point de vue, s’exprimer, défendre ses droits…
                    </p>
                    <p>
                        <b>Situation de révélation de soi</b> : parler de soi, faire connaissance…
                    </p>
                    <p><b>Situation d’interaction</b> : parler à une personne ou à un groupe.</p>
                    <p>
                        L’intensité de l’anxiété peut varier en fonction de le nombre de personne en face ainsi que de leur statut.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Trouble Anxieux généralisée (TAG)</li>
                <div class="definition">
                    <p>
                        L’anxiété est un phénomène normal auquel nous sommes confrontés lors de situations incertaines de notre quotidien : examen, déménagement, inquiétudes, un proche malade, risque de chômage…
                    </p>
                    <p>
                        L’anxiété nous permet d’être vigilant, d’agir et de répondre à ces situations problématiques.
                    </p>
                    <p>
                        <b>Le Trouble Anxieux Généralisé (TAG)</b> se caractérise par unb <b>état d’inquiétude permanent et excessif.</b> Le patient souffrant d’un TAG est anxieux devant des situations anodines de la vie quotidienne.
                    </p>
                    <p>Tout devient source d’angoisse : le retard d’un proche, l’absence de réponse immédiate à un SMS, les mauvaises nouvelles aux informations… <br>
                        Ce trouble anxieux généralisé peut impacter la vie professionnelle, sociale et affective.</p>

                </div>
            </div>
            <div class="text-wrap">
                <li>Stress, anxiété, trac</li>
                <div class="definition">
                    <p>
                        Le stress est la réponse de l’organisme aux situations et aux contraintes
                        perçues, en fonction des ressources de l’individu. Si la personne pense
                        être dans <b>l’impossibilité de répondre à une menace</b>, qu’elle soit <b>réelle
                            ou imaginaire</b>, alors il y aura du stress.
                    </p>
                    <p>
                        <b>Tout le monde</b> peut-être être touché par le stress. <b>Toutes les situations
                            peuvent être stressantes</b>, que ce soit un <b>événement positif</b>
                        (mariage, naissance, déménagement…) ou <b> négatif</b> (réunion importante,
                        problème financier…).
                    </p>
                    <p>
                        Le stress est une réaction tout à fait normale qui amène l’individu à
                        une résolution à court terme du problème. Une petite dose de stress peut
                        être bénéfique, cela mobilise les ressources, active la créativité etc…
                        Chronique, le stress, entretenu par l’exposition à des causes externes
                        (harcèlement) ou internes (rumination), peut entraîner un <b>déséquilibre psychologique</b>
                        (crises de panique, dépression par épuisement) et un <b>déséquilibre de son environnement</b>
                        (familial ou professionnel). <br>
                        <b>La santé de manière générale</b> peut être touchée (avec augmentation du risque cardio-vasculaire).
                    </p>


                </div>
            </div>
            <div class="text-wrap">
                <li> Trouble obsessionnel compulsif (TOC)</li>
                <div class="definition">
                    <p>
                        Il s’agit d’un trouble mental caractérisé par <b>l’apparition répétée de pensées intrusives,
                            les obsessions </b>, produisant de l’inconfort, de l’inquiétude, ou de la peur.
                        Cela se manifeste le plus souvent par des <b>comportements répétés et ritualisés, les compulsions. </b>
                        Ceux-ci peuvent avoir l’effet de diminuer l’anxiété ou de soulager une tension.
                    </p>
                    <p>
                        Les obsessions et les compulsions sont souvent associées et sont généralement reconnues comme <b>irrationnelles</b> par les personnes sujettes au TOC mais sont néanmoins <b>irrépressibles et envahissantes</b>
                        , diminuant le temps disponible pour d’autres activités.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Addiction, dépendance</li>
                <div class="definition">
                    <p>

                        <b>L’exemple de l’alcoolodépendance : <br></b>
                        Il y a alcoolodépendance dès lors qu’il y a la perte de la liberté de s’abstenir d’alcool, accompagnée de complications médico-psycho-sociales. Il n’est pas question d’un « manque de volonté » ou un « défaut de caractère »,
                        mais bien d’une maladie grave, progressive, chronique et terminale.
                    </p>
                    <p>
                        C’est une maladie multifactorielle, tant en ce qui concerne les causes, manifestations et effets. <br>
                        Elle entraîne des répercussions sur toutes ses dimensions (santé physique et psychique, personnalité, relations avec l’entourage, situation sociale, économique…) ainsi que son entourage et la société dans son ensemble.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Troubles du comportement alimentaire</li>
                <div class="definition">
                    <p>
                        La fréquence des <b>troubles du comportement alimentaire</b> augmente
                        constamment
                        dans notre société. Les causes de ces troubles sont induits par les comportements de <b>restriction alimentaire</b>
                        (régime), <b>la peur de grossir, l’insatisfaction de son corps comparé aux modèles imposés par la société</b>
                        (mannequin), <b>la difficulté à faire face aux conflits et à gérer les émotions. </b>
                        Ces troubles provoquent des altérations nutritionnelles:
                    <ul>
                        <li><b>Dénutrition</b> dans le cas de l’anorexie mentale</li>
                        <li><b>Carences</b> dans les cas de boulimie</li>
                        <li> <b>Surpoids</b>, obésité et leurs complications en cas de compulsions alimentaires. </li>
                    </ul>
                    </p>


                </div>
            </div>

            <div class="text-wrap">
                <li>Difficultés dans la gestion des émotions, colère, agacement, irritabilité</li>
                <div class="definition">
                    <p>
                        La colère est une émotion liée à une insatisfaction, à une blessure,
                        une injustice ou une frustration. La colère n’est jamais le problème,
                        il est normale de la ressentir de temps en temps, c’est une réaction à un événement.
                        Ce n’est jamais l’événement en soi qui déclenche la colère. <b>Elle est toujours une rencontre
                            entre un désir, une attente (irrationnelle ou pas) et un événement contraire à nos attentes. </b>
                    </p>
                    <p>
                        La colère peut être saine, elle nous aide à nous faire respecter des autres ou faire respecter nos besoins lorsqu’ils ont été bafoués. <br>
                        Le problème est souvent ce que l’on fait de cette colère. L’objectif est de réussir à mieux la gérer.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Affirmation de soi, confiance en soi, estime de soi</li>
                <div class="definition">
                    <p>
                        <b>L’affirmation de soi est</b> la <b>capacité à exprimer son opinion, ses sentiments et ses besoins. </b><br>
                        Lorsque nous n’exprimons pas clairement ce que nous ressentons, ce que nous pensons, nous perdons confiance en nous, nous éprouvons des émotions négatives comme la culpabilité, la frustration, la colère ou l’inquiétude.
                    </p>
                    <p>
                        L’affirmation de soi se développe au contact des autres, c’est un apprentissage. Il peut s’effectuer à tout moment de la vie. S’affirmer, c’est prendre soin de soi. Nous avons tous des droits et des besoins que nous devons respecter.
                    </p>
                    <p>
                        Le droit d’exprimer nos émotions <br>
                        Le droit de faire des erreurs <br>
                        Le droit de prendre soin de soi <br>
                        Le droit de dire ce que l’on souhaite, ce dont on a besoin, ce que l’on pense <br>
                        Le droit d’accepter de l’aide <br>
                        Le droit de dire non et de formuler des demandes
                    </p>
                    <p>
                        Nous pouvons repérer quatre types de comportements de communication : <br>
                    <p><b>Agressivité</b> : Je fais passer mes besoins avant ceux des autres, je me place au dessus des autres,</p>
                    <p>Passivité : Je fais passer les besoins des autres avant les miens, je me place en dessous des autres,</p>
                    <p><b>Manipulation</b> : je passe par des moyens détournés pour atteindre mes objectifs sans m’exprimer clairement,</p>
                    <p><b>Affirmation</b> : Je respecte mes besoins ainsi que ceux des autres, je me place en égal de l’autre. </p>
                    <hr>
                    <p>
                        Exemple :
                    <p> <b>Agressivité</b> : « Rends-toi utile, éteins la TV et va me chercher une baguette! »</p>
                    <p> <b>Passivité</b> : Je ne demande pas à l’autre et j’y vais moi même, même si je n’ai pas le temps.</p>
                    <p> <b>Manipulation</b> : « Je dois aller chercher du pain, mais j’ai un de ces boulot… Tu fais quoi, toi là? »</p>
                    <p> <b>Affirmation</b> : « Je suis ennuyée de te demander cela, mais je suis débordée. Pourrais tu me rendre service et aller chercher le pain? » </p>
                    </p>
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li>Dépendance affective, jalousie</li>
                <div class="definition">
                    <p>
                        La jalousie est une <b>émotion normale</b>, elle informe d’un danger ou d’une rupture affective & de la nécessité de protéger la relation.
                    </p>
                    <p>
                        <b>La jalousie devient pathologique quand elle n’est plus adaptée à la réalité</b>. Lorsqu’il n’y a pas de menace réelle quand la personne jalouse perd le contrôle.
                    </p>

                </div>
            </div>
            <div class="text-wrap">
                <li>La dépression/ tristesse</li>
                <div class="definition">
                    <p>
                        Le fait de se sentir triste, d’être « déprimé », d’avoir des « coups de blues » ne veut pas forcément dire
                        que l’on souffre de <b>dépression</b>. Les moments de questionnement font partie de la vie. Au cours de notre vie, chacun de nous expérimentera la tristesse, le découragement…
                    </p>
                    <p>
                        La dépression engendre une modification durable de l’humeur ainsi qu’un ralentissement moteur et cérébral. La personne souffrant de ce trouble est triste tous les jours et perd son tonus habituel, elle n’a plus envie de rien, peine à effectuer ses activités quotidiennes et y prend moins de plaisir ou même plus du tout.
                    </p>
                </div>
            </div>

            <div class="text-wrap">
                <li> Burn-out, épuisement professionnel</li>
                <div class="definition">
                    <p>
                        Le burn-out est un processus qui s’installe petit à petit chez les travailleurs qui sont soumis de façon prolongée au stress. Il s’agit d’un stress chronique dans le travail, la personne ne parvient plus à relâcher la pression.
                    </p>
                    <p>
                        Le burn-out évolue par étapes. Il est très important de réagir suffisamment tôt pour en limiter les répercussions.
                    </p>
                </div>
            </div>
            <div class="text-wrap">
                <li> Trouble du sommeil</li>
                <div class="definition">
                    <p>Les troubles du sommeil sont souvent lié aux habitudes du sommeil d’une personne. Ils peuvent sur le long terme interférer dans le fonctionnement physique, mental et émotionnel.
                        Il y a trois groupe de troubles du sommeil : <br></p>
                    <p>
                        <b>Les dyssomnies</b> : insomnie d’origine psychologique (incapacité à dormir la nuit), insomnie d’origine extérieure, troubles du sommeil liés à une substance, narcolepsie. On distingue l’insomnie de début de sommeil, l’insomnie de maintien de sommeil et l’insomnie de réveil précoce. Au delà 3 semaines, on parle d’insomnies chroniques,
                    </p>
                    <p><b>Les parasomnies</b> sont des troubles du sommeil associés à des réveils nocturnes sans véritable perturbation du sommeil. Il sont présents surtout chez l’enfant mais peuvent persister chez l’adulte. Le somnambulisme, les terreurs nocturnes, les troubles du sommeil associés au sommeil paradoxal, le bruxisme nocturne et l’énurésie nocturne font partie de cette catégorie. </p>
                    <p>
                        <b>Les troubles du sommeil d’origine psychiatrique</b>, neurologique ou secondaire à une autre pathologie (dépression, anxiété…).
                    </p>
                </div>
            </div>

        </ul>

    </div>
