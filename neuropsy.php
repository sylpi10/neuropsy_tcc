                La remédiation cognitive est un outil clinique destiné à renforcer les ressources cognitives, stimuler les fonctions cognitives afin de restaurer la capacité à percevoir, à comprendre, à décider et à agir. Elle vise à réduire les conséquences de troubles cognitifs pouvant avoir un fort retentissement négatif sur la capacité des personnes à affronter le quotidien, leurs relations interpersonnelles et à s’insérer professionnellement.
            </p>
            <p>
                La remédiation cognitive intervient après un bilan neuropsychologique préalable, lorsque la pathologie est stabilisée et que le traitement médicamenteux a été réduit à la posologie minimale efficace.
            </p>
            <p>
                Les compétences acquises en séances sont destinées à se généraliser et à permettre d’affronter avec plus de succès les situations quotidiennes, favorisant ainsi le rétablissement. Les programmes de remédiation cognitive reposent sur la réalisation d’exercices spécifiques verbaux, écrits ou informatisés, dans le cadre de séances collectives ou individuelles.
