            présentes conditions générales.</li> </ul>
    </div>

    <div class="text">
        <h5>Article 2 : Inscription</h5>
        <ul>
            <li> Les inscriptions peuvent se faire par e-mail ou par courrier, en remplissant le bulletin d'inscription mis à disposition.
                </li>
            <li>L'inscription sera considérée comme définitive à réception du contrat signé et du paiement de la formation, tel que précisé dans les modalités de paiement</li>
        </ul>
    </div>

    <div class="text">
        <h5>Article 3: Modalités de paiement</h5>
        <ul>
            <li>Le prix de la formation est indiqué sur le devis fourni au Stagiaire.</li>
            <li>Le paiement du montant total est exigé à la signature du contrat d'inscription.</li>
            <li>Les paiements peuvent être effectués par chèque, virement bancaire ou tout autre moyen spécifié dans le
                devis.</li>
        </ul>
    </div>
    <div class="text">
        <h5>Article 4 : Annulation et remboursement</h5>
        <ul>
            <li>En cas d'annulation par le Stagiaire, celui-ci doit en informer l'organisme de formation par écrit (e-mail ou
                courrier)</li>
            <ul>
                <li>Remboursement de 50% si l’annulation est faite au moins 14 jours avant le début de la formation.</li>
                <li>Remboursement de 30% si l’annulation est faite entre 14 et 7 jours avant le début de la formation.</li>
                <li>Aucun remboursement ne sera effectué si l'annulation est faite moins de 7 jours avant le début de la formation.</li>
            </ul>
        <li>L’organisme de formation « Psycho-Parent-Alp » se réserve le droit d'annuler une formation en cas de force
            majeure ou de nombre insuffisant de participants. Dans ce cas, le Stagiaire sera remboursé de l'intégralité des
            frais d'inscription.</li>
        <li>Attention, ces sommes ne sont pas imputables sur les fonds de la formation professionnelle continue et ne
            peuvent donc pas faire l’objet d’une demande de remboursement ou de prise en charge par l’OPCO.</li>
        </ul>
    </div>
    <div class="text">
        <h5>Article 5 : Déroulement de la formation</h5>
