    <div class="text">
        <h5>Article 1 : Champ d'application</h5>
        <ul> <li>Les présentes conditions générales de vente régissent les relations contractuelles entre l'organisme de formation
            Psycho-Parent-Alp fondé par Emmanuelle MIR et toute personne physique ou morale souhaitant s'inscrire à une
